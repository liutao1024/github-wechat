package cn.spring.mvn.web.zport;

public class UserinInput {
	private String cropno;
	private String userid;
	private String passwd;
	private boolean pswdfg;
	
	public String getCropno() {
		return cropno;
	}
	public void setCropno(String cropno) {
		this.cropno = cropno;
	}
	public String getUserid() {
		return userid;
	}
	public void setUserid(String userid) {
		this.userid = userid;
	}
	public String getPasswd() {
		return passwd;
	}
	public void setPasswd(String passwd) {
		this.passwd = passwd;
	}
	public boolean getPswdfg() {
		return pswdfg;
	}
	public void setPswdfg(boolean pswdfg) {
		this.pswdfg = pswdfg;
	}
	
}
